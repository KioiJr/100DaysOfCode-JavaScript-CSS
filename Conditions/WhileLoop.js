// Repeats execution as long as the condition is true.
// Has three steps: 1- Initialization. 2- Condition. 3- Increment.
//Checks the condition first then executes. If the condition is false, execution does not occur.

let i = 1; //Initialization

while(i<=5){ //Condition

    console.log("Hello",i);

    ++i; //Increment.
    //You can use post increment as well(i++)
}